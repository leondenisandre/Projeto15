package br.com;

public class Exemplares {
	   private long codigo;
	    private String n_exemplar;
	    private int t_exemplar;
	     
	    

	    public long getCodigo() {
	        return codigo;
	    }

	    public void setCodigo(long codigo) {
	        this.codigo = codigo;
	    }

	    public String getN_exemplar() {
	        return n_exemplar;
	    }

	    public void setN_exemplar(String n_exemplar) {
	        this.n_exemplar = n_exemplar;
	    }

	    public int getT_exemplar() {
	        return t_exemplar;
	    }

	    public void setT_exemplar(int t_exemplar) {
	        this.t_exemplar = t_exemplar;
	    }

}
